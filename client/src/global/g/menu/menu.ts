import { MenuRoles } from './menu.types';

let _menuRoles: MenuRoles = {};

const menu = {
  /********************************************************************************************************************
   * 메뉴
   * ******************************************************************************************************************/
  Admin: {
    // 어드민 관리
    Menu: 'admin/menu', // 메뉴 관리
    User: 'admin/user', // 사용자 관리
    Group: 'admin/group', // 그룹 관리
    LoginLog: 'admin/login_log', // 로그인 로그
    Password: 'admin/password', // 비밀번호 수정
    AccessLog: 'admin/access_log', // 사용 로그
    PrivacyAccessLog: 'admin/privacy_access_log', // 개인정보 조회 로그
  },

  /********************************************************************************************************************
   * 메뉴 권한 설정
   * ******************************************************************************************************************/
  setRoles(menuRoles: MenuRoles) {
    _menuRoles = menuRoles;
  },

  /********************************************************************************************************************
   * 메뉴 권한 있는지 여부 반환
   * ******************************************************************************************************************/
  hasRole(menu: string, role: 'read' | 'write' | 'export') {
    return _menuRoles[menu] ? _menuRoles[menu][role] : false;
  },

  /********************************************************************************************************************
   * 메뉴 Read 권한 있는지 여부 반환
   * ******************************************************************************************************************/
  hasReadRole(menu: string) {
    return this.hasRole(menu, 'read');
  },

  /********************************************************************************************************************
   * 메뉴 Write 권한 있는지 여부 반환
   * ******************************************************************************************************************/
  hasWriteRole(menu: string) {
    return this.hasRole(menu, 'write');
  },

  /********************************************************************************************************************
   * 메뉴 Export 권한 있는지 여부 반환
   * ******************************************************************************************************************/
  hasExportRole(menu: string) {
    return this.hasRole(menu, 'export');
  },
};

export default menu;
